#include "gpiote.h"

void gpiote_init(){
    //Knapp A hendelse
    GPIOTE->CONFIG[0] = 0x122E01; //0001 0010 0010 1110 0000 0001

    //Oppgaver koblet til forsyningspinne for LED-matrisen 
    GPIOTE->CONFIG[1] = 0x011501; //0000 0001 0001 0101 0000 0001
    GPIOTE->CONFIG[2] = 0x011601; //0000 0001 0001 0110 0000 0001
    GPIOTE->CONFIG[3] = 0x010F01; //0000 0001 0000 1111 0000 0001
    GPIOTE->CONFIG[4] = 0x011801; //0000 0001 0001 1000 0000 0001
    GPIOTE->CONFIG[5] = 0x011301; //0000 0001 0001 0011 0000 0001

}